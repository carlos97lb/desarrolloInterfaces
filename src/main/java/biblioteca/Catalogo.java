    private void jbPedirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbPedirActionPerformed
         int row = jTable.getSelectedRow();
         int column = jTable.getSelectedColumn();
        
        if(jTable.getValueAt(row,column)=="libre"){
             LogicaNegocio.getlistaLibros().get(row).setDisponibilidad(false);
             jTable.repaint();
             
         }else if(jTable.getValueAt(row,column)=="alquilado"){
             JOptionPane.showMessageDialog(this,"Este libro ya lo has alquilado" , "Pedido", JOptionPane.ERROR_MESSAGE); 
         }
    }//GEN-LAST:event_jbPedirActionPerformed

